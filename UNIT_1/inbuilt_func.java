import java.util.*;

public class inbulit_func
{
	public static void main(String[] args)
	{
		System.out.println("Max is "+Math.max(4,8));
		System.out.println("Min is "+Math.min(4,8));
		System.out.println("ABS is "+Math.abs(-48));
		System.out.println("Power is "+Math.pow(4,8));
		System.out.println("Square Root is "+Math.sqrt(4));
		System.out.println("Round is "+Math.round(4.8));
		System.out.println("Ceil is "+Math.ceil(4.8));
		System.out.println("Floor is "+Math.floor(4.8));
		System.out.printf("Random is : %.2f ",Math.random());
	}
}