public class Bitwiseoprtr
{
	
	public static void main(String[] args)
	{
		int a = 60;
		int b = 13;
		System.out.println("Bitwise Operater");
		System.out.println("Bitwise & "+(a&b));
		System.out.println("Bitwise | "+(a|b));
		System.out.println("Bitwise ^ "+(a^b));
		System.out.println("Bitwise ~"+(~a));
		System.out.println("Bitwise >>"+(a>>2));
		System.out.println("Bitwise <<"+(a<<2));
	}
}